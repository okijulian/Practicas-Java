public class Tipovariables {
  
    public static void main(String[] args) {

        // tipos enteros
        int miVariable = 10;
        System.out.println(miVariable);
        // modificando el valor de la variable
        miVariable = 5;
        System.out.println(miVariable);

        
        // tipo cadenas o strings
        String miString="hola desde java";         
        System.out.println(miString);

        // var -- inferencia de tipos en java
        var miVariableVar=230;
        System.out.println(miVariableVar);

        var miVariableVarString="variable string";
        System.out.println(miVariableVarString);


    }
    
}
